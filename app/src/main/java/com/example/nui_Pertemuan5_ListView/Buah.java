package com.example.nui_Pertemuan5_ListView;

public class Buah {

    String namaBuah, kaloriBuah, kandunganBuah, manfaatBuah;
    int gbrId;

    public Buah(String namaBuah, String kaloriBuah, String kandunganBuah, String manfaatBuah, int gbrId) {
        this.namaBuah = namaBuah;
        this.kaloriBuah = kaloriBuah;
        this.kandunganBuah = kandunganBuah;
        this.manfaatBuah = manfaatBuah;
        this.gbrId = gbrId;
    }

}
